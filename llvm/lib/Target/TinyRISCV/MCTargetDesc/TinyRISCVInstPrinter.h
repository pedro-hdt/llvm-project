#ifndef LLVM_LIB_TARGET_TinyRISCV_MCTARGETDESC_TinyRISCVINSTPRINTER_H
#define LLVM_LIB_TARGET_TinyRISCV_MCTARGETDESC_TinyRISCVINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class TinyRISCVInstPrinter : public MCInstPrinter {
public:
  explicit TinyRISCVInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                          const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}
  void printOperand(const MCInst *MI, unsigned OpNo, const MCSubtargetInfo &STI,
                    raw_ostream &O, const char *Modifier = nullptr);
  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, uint64_t Address,
                        const MCSubtargetInfo &STI, raw_ostream &O);
  bool printAliasInstr(const MCInst *MI, const MCSubtargetInfo &STI,
                       raw_ostream &O);
  void printCustomAliasOperand(const MCInst *MI, unsigned OpIdx,
                               unsigned PrintMethodIdx,
                               const MCSubtargetInfo &STI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
  static const char *getRegisterName(unsigned RegNo, unsigned AltIdx);
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_TinyRISCV_MCTARGETDESC_TinyRISCVINSTPRINTER_H
